#include "rush_01.h"

void	ft_init_arr(int arr[4][4])
{
	int y;
	int x;

	y = 0;
	x = 0;
	while (y < 4)
	{
		x = 0;
		while (x < 4)
		{
			arr[y][x] = 0;
			x++;
		}
		y++;
	}
}
