char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'A' && str[i] <= 'Z' && i != 0)
		{
			str[i] += 32;
		}
		else if ((((i == 0 && str[i] >= 'a' && str[i] <= 'z')
						|| (str[i - 1] == ' ' && str[i] >= 'A' && str[i] <= 'Z')
						|| (str[i - 1] < '0' && str[i] >= 'a' && str[i] <= 'z')
						|| (str[i - 1] > '9' && str[i - 1] < 'A'
							&& str[i] >= 'a' && str[i] <= 'z')
						|| (str[i - 1] > 'z' && str[i] >= 'a' && str[i] <= 'z'))
					&& (!(str[i] < 32 || str[i] > 126))))
		{
			str[i] -= 32;
		}
		i++;
	}
	return (str);
}
