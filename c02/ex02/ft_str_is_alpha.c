int		ft_str_is_alpha(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!('A' <= str[i] && str[i] <= 'z'))
		{
			return (0);
		}
		else if ('Z' < str[i] && str[i] < 'a')
		{
			return (0);
		}
		else
		{
			i++;
		}
	}
	return (1);
}
