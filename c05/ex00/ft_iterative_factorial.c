int	ft_iterative_factorial(int nb)
{
	int i;

	i = nb;
	if (nb < 0)
		return (0);
	if (nb == 1 | nb == 0)
		return (1);
	while (nb != 1)
	{
		nb--;
		i *= nb;
	}
	return (i);
}
