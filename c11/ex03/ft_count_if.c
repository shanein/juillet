int		ft_count_if(char **tab, int length, int (*f)(char*))
{
	int k;
	int nb;

	nb = 0;
	k = 0;
	while (length > k)
	{
		if ((f)(tab[k]) != 0)
			nb++;
		k++;
	}
	return (nb);
}
